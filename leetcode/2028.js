/**
 * @param {number[]} rolls
 * @param {number} mean
 * @param {number} n
 * @return {number[]}
 */
var missingRolls = function (rolls, mean, n) {
  let mSum = 0;

  for (let i = 0; i < rolls.length; i++) {
    mSum += rolls[i];
  }

  let nSum = mean * (rolls.length + n) - mSum;

  return genList(nSum, n);
};

const genList = (nSum, n) => {
  let nMean = nSum / n;
  if (nMean < 1 || nMean > 6) return [];

  let rest = nSum % n;
  if (rest === 0) return new Array(n).fill(nMean);


  const num = Math.floor(nMean);

  return new Array(rest)
    .fill(num + 1)
    .concat(new Array(n - rest).fill(num));
};

